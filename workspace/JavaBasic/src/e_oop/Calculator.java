package e_oop;

public class Calculator {

	//두 값의 산술연산을 수행하는 메서드들을 만들어주세요.
	
	double plusMethod(double a, double b)
	{
		return a+b;
	}

	double minusMethod(double a, double b)
	{
		return a-b;
	}
	
	double multipleMethod(double a, double b)
	{
		return a*b;
	}
	
	double divideMethod(double a, double b)
	{
		return a/b;
	}
	
	double remMethod(double a, double b)
	{
		return a%b;
	}
}
